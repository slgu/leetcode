    public int judge(int left, int right){
        int flg = 0;
        while(left <= right){
            int middle = (left + right) >> 1;
            if(nums[middle] == target)
                return 2;
            if(nums[middle] > nums[left]){
                flg = 1;
                if(target > nums[middle]){
                    left = middle + 1;
                }
                else{
                    if(target == nums[left])return 2;
                    if(target > nums[left]){
                        right = middle - 1;
                    }
                    else{
                        left = middle + 1;
                    }
                }
            }
            else if(nums[middle] < nums[left]){
                flg = 1;
                if(target < nums[middle]){
                    right = middle - 1;
                }
                else{
                    if(target == nums[right]) return 2;
                    if(target < nums[right]){
                        left = middle + 1;
                    }
                    else{
                        right = middle - 1;
                    }
                }
            }
            else{
                int res = judge(left, middle - 1);
                if(res == 0){
                    return judge(middle + 1, right);
                }
                else
                    return res;
            }
        }
        return flg;
    }
